export function getContractorMetrics(data: Contractor[]) {
  const byPriority = {
    "P.01 (Very Urgent)":  data.filter((c) => c.priority === "P.01 (Very Urgent)").length,
    "P.02 (Urgent)":       data.filter((c) => c.priority === "P.02 (Urgent)").length,
    "P.03 (ASAP)":         data.filter((c) => c.priority === "P.03 (ASAP)").length,
    "P.04 (As Planned)":   data.filter((c) => c.priority === "P.04 (As Planned)").length,
  };

  const byStatus = {
    Searching:  data.filter((c) => c.hiringStatus === "Candidates (Searching)").length,
    Interviews: data.filter((c) => c.hiringStatus === "Candidates (Interviews)").length,
    Closed:     data.filter((c) => c.hiringStatus === "Candidates (Closed)").length,
    Onboarding: data.filter((c) => c.hiringStatus === "Candidates (Onboarding)").length,
    Pending:    data.filter((c) => c.hiringStatus === "Pending Deal Status").length,
  };

  const byLocation = Array.from(
    data.reduce((m, c) => {
      m.set(c.location, (m.get(c.location) ?? 0) + 1);
      return m;
    }, new Map<string, number>())
  ).map(([location, count]) => ({ location, count })).sort((a, b) => b.count - a.count);

  const initiatives = [...new Set(data.map((c) => c.initiative))];

  return { byPriority, byStatus, byLocation, initiatives };
}
